    public static String removeDuplicateSentences(String text, int threshold) {
        // 1. 按句子分割
        String[] sentences = text.split("[.!?]\\s*");

        // 2. 统计每个句子出现次数
        Map<String, Integer> sentenceCount = new HashMap<>();
        for (String s : sentences) {
            sentenceCount.put(s, sentenceCount.getOrDefault(s, 0) + 1);
        }

        // 3. 构建处理后的句子列表
        List<String> resultSentences = new ArrayList<>();
        for (String s : sentences) {
            if (sentenceCount.get(s) <= threshold) {
                resultSentences.add(s);
            }
        }

        // 4. 拼接结果
        StringBuilder sb = new StringBuilder();
        for (int i = 0; i < resultSentences.size(); i++) {
            sb.append(resultSentences.get(i));
            if (i != resultSentences.size() - 1) {
                sb.append(". ");
            }
        }
        return sb.toString();
    }

